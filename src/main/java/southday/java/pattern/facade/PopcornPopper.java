package southday.java.pattern.facade;

/**
 * 爆米花机
 * @author southday
 * @date 2018年6月3日
 */
public class PopcornPopper {
    public void on() {
        System.out.println("打开爆米花机");
    }
    
    public void off() {
        System.out.println("关闭爆米花机");
    }
    
    public void pop() {
        System.out.println("炸出爆米花");
    }
}
