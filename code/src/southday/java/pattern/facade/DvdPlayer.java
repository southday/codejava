package southday.java.pattern.facade;

public class DvdPlayer {
    public void on() {
        System.out.println("打开DVD");
    }
    
    public void off() {
        System.out.println("关闭DVD");
    }
    
    public void play() {
        System.out.println("播放DVD");
    }
    
    public void stop() {
        System.out.println("停止播放");
    }
}
